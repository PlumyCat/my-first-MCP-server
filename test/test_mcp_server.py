class MCPPerfectClient:
    """Client MCP parfait avec extraction correcte"""
    
    def __init__(self):
        self.process = None
        self.message_id = 0
        self.initialized = False
        self.stats = {
            "tests_total": 0,
            "tests_success": 0,
            "temps_moyen": 0,
            "temperatures": []
        }
    
    def get_next_id(self):
        self.message_id += 1
        return self.message_id
    
    async def start_server(self) -> bool:
        try:
            print("üöÄ D√©marrage du serveur MCP...")
            
            env = os.environ.copy()
            env["PYTHONPATH"] = WORKSPACE_PATH
            env["PYTHONUNBUFFERED"] = "1"
            
            self.process = await asyncio.create_subprocess_exec(
                *SERVER_COMMAND,
                cwd=WORKSPACE_PATH,
                env=env,
                stdin=asyncio.subprocess.PIPE,
                stdout=asyncio.subprocess.PIPE,
                stderr=asyncio.subprocess.PIPE
            )
            
            print(f"   ‚úÖ Serveur d√©marr√© (PID: {self.process.pid})")
            return True
            
        except Exception as e:
            print(f"   ‚ùå Erreur: {e}")
            return False
    
    async def send_message(self, message: Dict[str, Any]) -> Dict[str, Any]:
        if not self.process:
            return {"error": "Server not started"}
        
        try:
            message_json = json.dumps(message) + "\n"
            
            self.process.stdin.write(message_json.encode())
            await self.process.stdin.drain()
            
            response_line = await asyncio.wait_for(
                self.process.stdout.readline(), 
                timeout=10.0
            )
            
            if response_line:
                return json.loads(response_line.decode().strip())
            else:
                return {"error": "No response"}
                
        except asyncio.TimeoutError:
            return {"error": "Timeout"}
        except Exception as e:
            return {"error": str(e)}
    
    async def send_notification(self, method: str, params: Dict[str, Any] = None):
        if not self.process:
            return
        
        try:
            notification = {"jsonrpc": "2.0", "method": method}
            if params is not None:
                notification["params"] = params
            
            notification_json = json.dumps(notification) + "\n"
            self.process.stdin.write(notification_json.encode())
            await self.process.stdin.drain()
            
        except Exception as e:
            print(f"   ‚ö†Ô∏è Erreur notification: {e}")
    
    async def initialize(self) -> bool:
        try:
            print("ü§ù Initialisation du protocole MCP...")
            
            # Initialize
            init_message = {
                "jsonrpc": "2.0",
                "id": self.get_next_id(),
                "method": "initialize",
                "params": {
                    "protocolVersion": "2024-11-05",
                    "capabilities": {"tools": {}},
                    "clientInfo": {"name": "final-test-client", "version": "1.0.0"}
                }
            }
            
            init_response = await self.send_message(init_message)
            if "error" in init_response:
                print(f"   ‚ùå Erreur initialize: {init_response['error']}")
                return False
            
            # Initialized notification
            await self.send_notification("notifications/initialized")
            await asyncio.sleep(0.5)
            
            self.initialized = True
            print("   ‚úÖ Protocole MCP initialis√©")
            return True
            
        except Exception as e:
            print(f"   ‚ùå Erreur initialisation: {e}")
            return False
    
    async def get_tools(self) -> List[Dict[str, Any]]:
        if not self.initialized:
            return []
        
        try:
            print("üìã R√©cup√©ration des outils disponibles...")
            
            list_message = {
                "jsonrpc": "2.0",
                "id": self.get_next_id(),
                "method": "tools/list",
                "params": {}
            }
            
            response = await self.send_message(list_message)
            
            if "result" in response:
                tools = response["result"].get("tools", [])
                print(f"   ‚úÖ {len(tools)} outil(s) disponible(s)")
                
                for tool in tools:
                    name = tool.get("name", "Unknown")
                    description = tool.get("description", "No description")
                    print(f"      üõ†Ô∏è {name}: {description}")
                
                return tools
            else:
                print(f"   ‚ùå Erreur: {response.get('error')}")
                return []
                
        except Exception as e:
            print(f"   ‚ùå Exception: {e}")
            return []
    
    def extract_weather_data(self, response: Dict[str, Any]) -> Dict[str, Any]:
        """Extraction parfaite des donn√©es m√©t√©o (format valid√©)"""
        try:
            if "result" in response:
                result = response["result"]
                
                # Format valid√© : result -> content -> [0] -> text -> JSON
                if isinstance(result, dict) and "content" in result:
                    content = result["content"]
                    if isinstance(content, list) and len(content) > 0:
                        text_item = content[0]
                        if isinstance(text_item, dict) and "text" in text_item:
                            weather_json = text_item["text"]
                            return json.loads(weather_json)
            
            return {"success": False, "error": "Format de r√©ponse invalide"}
            
        except json.JSONDecodeError as e:
            return {"success": False, "error": f"JSON invalide: {e}"}
        except Exception as e:
            return {"success": False, "error": f"Erreur extraction: {e}"}
    
    async def call_weather_tool(self, city: str, unit: str) -> Dict[str, Any]:
        """Appel de l'outil m√©t√©o avec mesure de performance"""
        if not self.initialized:
            return {"success": False, "error": "Client non initialis√©"}
        
        import time
        start_time = time.time()
        
        try:
            call_message = {
                "jsonrpc": "2.0",
                "id": self.get_next_id(),
                "method": "tools/call",
                "params": {
                    "name": "get_weather",
                    "arguments": {"city": city, "unit": unit}
                }
            }
            
            response = await self.send_message(call_message)
            
            # Mesurer le temps de r√©ponse
            response_time = time.time() - start_time
            
            # Extraire les donn√©es
            weather_data = self.extract_weather_data(response)
            
            # Ajouter les statistiques
            if weather_data.get("success"):
                weather_data["response_time"] = response_time
                self.stats["temps_moyen"] += response_time
                self.stats["temperatures"].append(weather_data["data"]["temperature"])
            
            return weather_data
            
        except Exception as e:
            return {"success": False, "error": str(e)}
    
    async def stop_server(self):
        if self.process:
            print("\nüõë Arr√™t du serveur...")
            self.process.terminate()
            try:
                await asyncio.wait_for(self.process.wait(), timeout=3.0)
                print("   ‚úÖ Serveur arr√™t√© proprement")
            except asyncio.TimeoutError:
                self.process.kill()
                print("   ‚ö†Ô∏è Arr√™t forc√©")


